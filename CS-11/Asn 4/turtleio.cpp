#include "turtlelib.cpp"

int ccc_win_main() {
    Turtle tattle;
    
    int num1 = tattle.getInt("Enter an integer: ");
    tattle.moveTo(-8, 8);
    tattle.print(num1);
    
    double num2 = tattle.getDouble("Enter a double: ");
    tattle.moveTo(-8, 7);
    tattle.print(num2);

    string msg = tattle.getString("Enter a message: ");
    tattle.moveTo(-8, 6);
    tattle.print(msg);
    
    tattle.sleep(1000);
    tattle.moveTo(-9, 5);
    tattle.right(90);
    tattle.forward(5);
    tattle.left(90);
    tattle.forward(4);
    tattle.left(90);
    tattle.forward(5);
    tattle.left(90);
    tattle.forward(4);
    tattle.left(90);

    return 0;
}